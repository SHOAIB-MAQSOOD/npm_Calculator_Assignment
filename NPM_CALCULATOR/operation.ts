export const Sum = (num1 : number, num2 : number):number => {
    return num1 + num2
}

export const Sub = (num1 : number, num2 : number):number => {
    return num1 - num2
}

export const multiply = (num1 : number, num2 : number):number => {
    return num1  *  num2
}

export const division = (num1 : number, num2 : number):number => {
    return num1 / num2
}


export default {Sum,Sub,multiply,division}

