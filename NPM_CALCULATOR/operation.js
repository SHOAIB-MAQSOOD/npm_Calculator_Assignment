export const Sum = (num1, num2) => {
    return num1 + num2;
};
export const Sub = (num1, num2) => {
    return num1 - num2;
};
export const multiply = (num1, num2) => {
    return num1 * num2;
};
export const division = (num1, num2) => {
    return num1 / num2;
};
export default { Sum, Sub, multiply, division };
